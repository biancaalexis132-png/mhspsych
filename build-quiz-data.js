#!/usr/bin/env node
// Parses every /quizzes/<slug>.html and writes quiz-data.js
// containing window.QUIZ_DATA = { slug: {title, category, questions:[...]} }
// Each question: { id, stem, options:[{id,text}], correct:[id], explanation }

const fs = require('fs');
const path = require('path');

const DIR = path.join(__dirname, 'quizzes');
const OUT = path.join(__dirname, 'quiz-data.js');

function decode(s) {
  return s
    .replace(/&amp;/g, '&')
    .replace(/&lt;/g, '<')
    .replace(/&gt;/g, '>')
    .replace(/&quot;/g, '"')
    .replace(/&#39;/g, "'")
    .replace(/&nbsp;/g, ' ');
}

// Strip simple inline tags (<strong>, <em>, <code>) and return plain text.
function stripTags(s) {
  return decode(s.replace(/<[^>]+>/g, '').trim()).replace(/\s+/g, ' ');
}

function parseQuizFile(filePath) {
  const html = fs.readFileSync(filePath, 'utf8');

  // Title and category from header
  const titleMatch = html.match(/<h1 class="quiz-title">([\s\S]*?)<\/h1>/);
  const catMatch = html.match(/<div class="quiz-category">([\s\S]*?)<\/div>/);
  const title = titleMatch ? stripTags(titleMatch[1]) : '';
  const category = catMatch ? stripTags(catMatch[1]) : '';

  // Each question block
  const qBlockRe = /<div class="quiz-question" id="q(\d+)">([\s\S]*?)(?=<div class="quiz-question" id="q\d+">|<\/div>\s*<div class="results-card"|<\/div>\s*<\/div>\s*<div class="results-card")/g;
  const questions = [];

  // Simpler split approach: find each quiz-question, then peel off options until next quiz-question or </div> closing quiz-container.
  const blocks = html.split(/<div class="quiz-question" id="q\d+">/).slice(1);
  for (let i = 0; i < blocks.length; i++) {
    const raw = blocks[i];
    // End of this block is start of next quiz-question div or end of quiz-container
    const stemMatch = raw.match(/<div class="quiz-stem">([\s\S]*?)<\/div>/);
    if (!stemMatch) continue;
    let stem = stripTags(stemMatch[1]);
    // Strip leading "1. " numbering
    stem = stem.replace(/^\d+\.\s*/, '');

    const optRe = /<div class="quiz-option" data-correct="(true|false)"[^>]*>[\s\S]*?<span>([\s\S]*?)<\/span>\s*<\/div>/g;
    const options = [];
    const correct = [];
    let m;
    let idx = 0;
    while ((m = optRe.exec(raw)) !== null) {
      const isCorrect = m[1] === 'true';
      let text = stripTags(m[2]);
      // Strip leading "A. " letter prefix
      text = text.replace(/^[A-Z]\.\s*/, '');
      const id = String.fromCharCode(97 + idx); // a, b, c, d, ...
      options.push({ id, text });
      if (isCorrect) correct.push(id);
      idx++;
      if (idx > 12) break; // safety
    }
    if (options.length === 0) continue;

    const explMatch = raw.match(/<div class="explanation-box"[^>]*>([\s\S]*?)<\/div>/);
    const explanation = explMatch ? stripTags(explMatch[1]) : '';

    questions.push({
      id: 'q' + (i + 1),
      stem,
      options,
      correct,
      explanation
    });
  }

  return { title, category, questions };
}

const data = {};
const files = fs.readdirSync(DIR).filter(f => f.endsWith('.html') && f !== 'index.html');
for (const f of files) {
  const slug = f.replace(/\.html$/, '');
  try {
    const parsed = parseQuizFile(path.join(DIR, f));
    if (parsed.questions.length === 0) {
      console.warn(`! ${slug}: no questions parsed`);
      continue;
    }
    data[slug] = parsed;
    console.log(`✓ ${slug}: ${parsed.questions.length} Qs`);
  } catch (e) {
    console.error(`✗ ${slug}: ${e.message}`);
  }
}

const banner = `// Auto-generated by build-quiz-data.js — do not edit by hand.
// Source: parsed from quizzes/*.html
// Each entry: { title, category, questions: [{ id, stem, options:[{id,text}], correct:[id], explanation }] }
window.QUIZ_DATA = `;

fs.writeFileSync(OUT, banner + JSON.stringify(data, null, 2) + ';\n');
console.log(`\nWrote ${OUT} (${Object.keys(data).length} quizzes)`);
